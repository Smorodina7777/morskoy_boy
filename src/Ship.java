public class Ship {
    byte ship_size;
    String ship_title;

    public Ship() {
        this.ship_size = ship_size;
    }

    public Ship(String ship_title) {
        this.ship_title = ship_title;
    }

    public String get_title() {
        return ship_title;
    }

    public byte get_size() {
        return ship_size;
    }
}
